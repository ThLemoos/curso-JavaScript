console.log("Thiago Lemos"); // Mostrar texto com aspas duplas
console.log('Thiago Lemos'); // Mostrar texto com aspas simples
console.log(`Thiago Lemos`); // Mostrar texto com crases
console.log("Thiago 'Lemos'"); // Aspas simples dentro de aspas duplas
console.log('Thiago "Lemos"'); // Aspas duplas dentro de aspas simples
console.log(`'Thiago'"Lemos"`); // Apas simples e aspas duplas dentro de crase
console.log(123456); // Mostrando números inteiros na tela
console.log(15.7) // Mostrando números reais ou de ponto flutuante na tela
console.log(123, 15.7, "Thiago Lemos"); // Mostrando diversos tipos de dados um ao lado do outro dentro de um console.log(), todos eles separados por vírgula